def analytics(account_id: int) -> dict:
    conn = _connect()
    try:
        languages = [
            {"language": r["reply_language"] or "unknown", "count": r["c"]}
            for r in conn.execute(
                "SELECT reply_language, COUNT(*) c FROM calls WHERE account_id = ? GROUP BY reply_language ORDER BY c DESC",
                (account_id,),
            ).fetchall()
        ]
        hours = [
            {"hour": int(r["h"]), "count": r["c"]}
            for r in conn.execute(
                "SELECT to_char(started_at::timestamp, 'HH24') h, COUNT(*) c FROM calls "
                "WHERE account_id = ? GROUP BY h ORDER BY h",
                (account_id,),
            ).fetchall()
        ]
        duration_trend = [
            {"day": r["day"], "avgSeconds": round(r["avg_s"] or 0, 1)}
            for r in conn.execute(
                """
                SELECT started_at::date::text AS day, AVG(duration_seconds) avg_s FROM calls
                WHERE account_id = ? AND started_at::date >= (CURRENT_DATE - INTERVAL '13 days')::date
                GROUP BY day ORDER BY day
                """,
                (account_id,),
            ).fetchall()
        ]
        rows = conn.execute("SELECT * FROM calls WHERE account_id = ?", (account_id,)).fetchall()
        sentiment = {"positive": 0, "neutral": 0, "negative": 0}
        engaged = 0
        for row in rows:
            transcript = json.loads(row["transcript_json"]) if row["transcript_json"] else []
            sentiment[_sentiment(transcript)] += 1
            if len(transcript) >= 4:
                engaged += 1
        # Calls + qualification split by channel (Web / Website Widget /
        # Phone) and by handling agent — the "which channel and which agent
        # actually convert" view the dashboard's analytics tab charts.
        by_channel = [
            {
                "channel": _CHANNEL_LABELS.get(r["t"], "Web"),
                "calls": r["c"],
                "qualified": r["q"] or 0,
                "minutes": round(r["s"] / 60, 1),
            }
            for r in conn.execute(
                """
                SELECT COALESCE(call_type, 'browser') t, COUNT(*) c,
                       SUM(CASE WHEN lead_name IS NOT NULL THEN 1 ELSE 0 END) q,
                       COALESCE(SUM(duration_seconds), 0) s
                FROM calls WHERE account_id = ? GROUP BY t ORDER BY c DESC
                """,
                (account_id,),
            ).fetchall()
        ]
        agent_names = _agent_names_by_id(account_id)
        fallback_agent = next(iter(agent_names.values()), "Agent")
        # Merge by display name, not agent_id: legacy rows (agent_id NULL,
        # from before the column existed) resolve to the first agent's name
        # and should count as that agent, not as a second phantom bar.
        by_agent_map: dict[str, dict] = {}
        for r in conn.execute(
            """
            SELECT agent_id, COUNT(*) c,
                   SUM(CASE WHEN lead_name IS NOT NULL THEN 1 ELSE 0 END) q,
                   COALESCE(SUM(duration_seconds), 0) s
            FROM calls WHERE account_id = ? GROUP BY agent_id
            """,
            (account_id,),
        ).fetchall():
            label = agent_names.get(r["agent_id"]) or fallback_agent
            entry = by_agent_map.setdefault(label, {"agent": label, "calls": 0, "qualified": 0, "seconds": 0.0})
            entry["calls"] += r["c"]
            entry["qualified"] += r["q"] or 0
            entry["seconds"] += r["s"]
        by_agent = [
            {"agent": e["agent"], "calls": e["calls"], "qualified": e["qualified"], "minutes": round(e["seconds"] / 60, 1)}
            for e in sorted(by_agent_map.values(), key=lambda e: -e["calls"])
        ]
        s = summary(account_id)
        return {
            "languages": languages,
            "peakHours": hours,
            "durationTrend": duration_trend,
            "sentiment": sentiment,
            "byChannel": by_channel,
            "byAgent": by_agent,
            "funnel": {
                "answered": s["totalCalls"],
                "engaged": engaged,
                "qualified": s["qualifiedCalls"],
                "visitBooked": s["siteVisits"],
            },
        }
    finally:
        conn.close()


