def _connect() -> dbconn.Conn:
    return dbconn.connect()


# Tables with a plain nullable account_id column whose pre-multi-tenant rows
# (from the SQLite era, carried over by the one-time data migration) need
# adopting by the first real account.
_BACKFILL_TABLES = ("agents", "sites", "inbound_routes", "campaigns", "phone_numbers", "calls", "knowledge_bases", "contacts")


def _backfill_legacy_account_id(conn: dbconn.Conn, account_id: int) -> None:
    """Every row left with account_id IS NULL predates multi-tenancy —
    hand them to `account_id` (the first account ever created), so existing
    data becomes that account's data rather than every tenant's data."""
    for table in _BACKFILL_TABLES:
        conn.execute(f"UPDATE {table} SET account_id = ? WHERE account_id IS NULL", (account_id,))


def provision_account_defaults(conn: dbconn.Conn, account_id: int) -> None:
    """Starter state for a brand-new (non-legacy) account: one default agent
    to test with, its own copy of the well-known integrations, and its own
    billing settings — mirrors what init_tables used to seed globally."""
    conn.execute(
        "INSERT INTO agents (account_id, name, description) VALUES (?, ?, ?)",
        (
            account_id,
            "Artha",
            "Default sales agent — qualifies leads and books appointments in 11 Indian languages.",
        ),
    )
    for key, name, category, description in _SEED_INTEGRATIONS:
        conn.execute(
            "INSERT INTO integrations (account_id, key, name, category, description) VALUES (?, ?, ?, ?, ?) "
            "ON CONFLICT (account_id, key) DO NOTHING",
            (account_id, key, name, category, description),
        )
    for key, value in (
        # Free trial allowance for a brand-new signup — deliberately small;
        # an operator upgrades the account's plan for more.
        ("credits_total", "10"),
        # Phone minutes burn more credits than browser/widget minutes — they
        # carry an EnableX telephony cost the others don't.
        ("credit_rate_browser", "1"),
        ("credit_rate_phone", "1.5"),
        ("credit_rate_widget", "1"),
    ):
        conn.execute(
            "INSERT INTO settings (account_id, key, value) VALUES (?, ?, ?) "
            "ON CONFLICT (account_id, key) DO NOTHING",
            (account_id, key, value),
        )


def init_tables() -> None:
    conn = _connect()
    try:
        with conn:
            conn.execute(_SCHEMA)
            # CREATE TABLE IF NOT EXISTS above is a no-op on a table that
            # already exists in production — new columns added after go-live
            # need an explicit migration to actually land on the live table.
            for column, coltype in (
                ("lead_company", "TEXT"),
                ("lead_use_case", "TEXT"),
                ("lead_team_size", "TEXT"),
                ("lead_email", "TEXT"),
                # JSON blob of post-call structured fields the agent extracted
                # from this call's transcript (per the agent's post_call_fields).
                ("extracted_data", "TEXT DEFAULT ''"),
                # Cached conversation-intelligence JSON (summary/sentiment/
                # outcome/qa_score/…) — populated on demand by call_intelligence.
                ("intelligence_json", "TEXT DEFAULT ''"),
                # Exact voice string this call used (e.g. "priya",
                # "elevenlabs:7b9m...") — captured at save time so a later
                # voice change on the agent never retroactively reclassifies
                # an old call's cost. NULL on every call recorded before this
                # column existed; voice_tier() below treats that as "standard"
                # so historical billing is unaffected.
                ("voice", "TEXT"),
                # Per-call ArthaLeads delivery outcome — separate from the
                # integration-level last_sync/last_error (which only reflect
                # the most recent attempt across ALL calls), so an operator
                # can see and act on exactly this lead's status. NULL means
                # never attempted (call wasn't qualified, or no integration
                # connected at call time) — not the same as "failed".
                ("arthaleads_status", "TEXT"),
                ("arthaleads_synced_at", "TEXT"),
                ("arthaleads_error", "TEXT"),
                # Backblaze B2 object key for this call's recording (see
                # agent/recording.py), set after the fact once the agent's
                # post-call upload finishes. Never returned to the frontend
                # directly — _call_dict below exposes only a hasRecording
                # bool; playback goes through a presigned-URL route instead.
                ("recording_key", "TEXT"),
            ):
                conn.execute(f"ALTER TABLE calls ADD COLUMN IF NOT EXISTS {column} {coltype}")
            conn.execute("ALTER TABLE agents ADD COLUMN IF NOT EXISTS is_platform_demo INTEGER DEFAULT 0")
            # Agent-builder settings added after go-live — explicit migrations so
            # they land on the existing production agents table (see agents CREATE
            # TABLE above for what each one does).
            for column, coltype in (
                ("first_speaker", "TEXT DEFAULT 'agent'"),
                ("welcome_message", "TEXT DEFAULT ''"),
                ("interruption_sensitivity", "REAL DEFAULT 0.5"),
                ("silence_reminder_ms", "INTEGER DEFAULT 0"),
                ("silence_reminder_max", "INTEGER DEFAULT 1"),
                ("end_call_on_silence_ms", "INTEGER DEFAULT 0"),
                ("max_call_duration_s", "INTEGER DEFAULT 0"),
                ("enabled_functions", "TEXT DEFAULT ''"),
                ("transfer_phone", "TEXT DEFAULT ''"),
                ("custom_functions", "TEXT DEFAULT '[]'"),
                ("post_call_fields", "TEXT DEFAULT '[]'"),
                # How strongly agent/emotion.py's live per-turn caller-emotion
                # detection shows up in delivery (voice_settings on ElevenLabs,
                # pace/pitch on Sarvam) — "off"/"subtle"/"strong". Defaults to
                # "strong" so every agent created before this field existed
                # keeps today's full-strength behavior unchanged.
                ("emotion_intensity", "TEXT DEFAULT 'strong'"),
                ("webhook_url", "TEXT DEFAULT ''"),
                ("memory_enabled", "INTEGER DEFAULT 0"),
            ):
                conn.execute(f"ALTER TABLE agents ADD COLUMN IF NOT EXISTS {column} {coltype}")
            conn.execute("ALTER TABLE accounts ADD COLUMN IF NOT EXISTS is_platform_owner INTEGER DEFAULT 0")
            # NULL until the first-run onboarding modal is dismissed. Every
            # pre-existing account (created before onboarding existed) should
            # count as already onboarded rather than suddenly seeing the
            # modal — but that backfill must run exactly once, the boot this
            # column is introduced, or every later boot would immediately
            # backfill brand-new accounts too (their created_at is already in
            # the past by the time this runs) and the modal would never show.
            column_existed = conn.execute(
                "SELECT 1 FROM information_schema.columns WHERE table_name = 'accounts' AND column_name = 'onboarded_at'"
            ).fetchone() is not None
            conn.execute("ALTER TABLE accounts ADD COLUMN IF NOT EXISTS onboarded_at TEXT")
            if not column_existed:
                conn.execute("UPDATE accounts SET onboarded_at = created_at WHERE onboarded_at IS NULL")
            # Super-admin support columns: tenant lifecycle status + private
            # operator notes on the account; per-user login tracking + which
            # method they authenticate with (surfaced in the admin panel).
            conn.execute("ALTER TABLE accounts ADD COLUMN IF NOT EXISTS status TEXT DEFAULT 'active'")
            conn.execute("ALTER TABLE accounts ADD COLUMN IF NOT EXISTS notes TEXT DEFAULT ''")
            conn.execute("ALTER TABLE users ADD COLUMN IF NOT EXISTS last_login_at TEXT")
            conn.execute("ALTER TABLE users ADD COLUMN IF NOT EXISTS auth_provider TEXT DEFAULT 'password'")
            # NULL until the first-run dashboard tour is finished or skipped.
            # Per-user (not per-account like onboarded_at above) so a
            # teammate invited into an already-onboarded account still sees
            # the tour once on their own first login.
            conn.execute("ALTER TABLE users ADD COLUMN IF NOT EXISTS tour_completed_at TEXT")
            # Surfaces *why* a live-call delivery to an integration failed
            # (e.g. "invalid token — reconnect") without flipping status away
            # from 'connected' — the operator's saved config is still good,
            # the last attempt just didn't land. Cleared on the next success.
            conn.execute("ALTER TABLE integrations ADD COLUMN IF NOT EXISTS last_error TEXT")
            # Campaign-engine columns added after the campaigns table shipped as
            # metadata-only — the real dialer needs a from-number and retry/
            # concurrency policy on the existing production table.
            for column, coltype in (
                ("from_number", "TEXT DEFAULT ''"),
                ("max_attempts", "INTEGER DEFAULT 1"),
                ("retry_minutes", "INTEGER DEFAULT 60"),
                ("concurrency", "INTEGER DEFAULT 1"),
                ("started_at", "TEXT"),
                ("completed_at", "TEXT"),
            ):
                conn.execute(f"ALTER TABLE campaigns ADD COLUMN IF NOT EXISTS {column} {coltype}")
            # "Premium+" (ElevenLabs v3) was folded into Premium (Flash v2.5) on
            # 2026-07-14 — v3's realtime endpoint 403s in production, so it was
            # never a good tier to keep selling (see voice_catalog.py's CATALOG
            # comment). Every v3 voice ID has an equivalent Premium catalog entry
            # now, so this is a pure prefix rewrite, not a voice change — self-
            # healing on every boot so any agent or account-voice-menu row still
            # holding the old prefix (set before this migration first ran)
            # follows automatically, with no separate one-off script needed.
            conn.execute(
                "UPDATE agents SET voice = 'elevenlabs:' || substring(voice from 15) "
                "WHERE voice LIKE 'elevenlabs-v3:%%'"
            )
            conn.execute(
                "UPDATE account_voices SET voice_string = 'elevenlabs:' || substring(voice_string from 15) "
                "WHERE voice_string LIKE 'elevenlabs-v3:%%' "
                "AND NOT EXISTS ("
                "  SELECT 1 FROM account_voices av2 WHERE av2.account_id = account_voices.account_id "
                "  AND av2.voice_string = 'elevenlabs:' || substring(account_voices.voice_string from 15)"
                ")"
            )
            conn.execute("DELETE FROM account_voices WHERE voice_string LIKE 'elevenlabs-v3:%%'")
            # Refresh the integration catalog for EVERY existing tenant (the
            # per-account seed only runs at signup). Inserts newly-added
            # integrations (Slack/WhatsApp) for accounts that lack them and
            # refreshes name/category/description on the seed rows — so old
            # rows like "ArthaleLeads webhook" pick up current branding —
            # without disturbing each tenant's status/config/last_sync.
            for skey, sname, scategory, sdescription in _SEED_INTEGRATIONS:
                conn.execute(
                    "INSERT INTO integrations (account_id, key, name, category, description) "
                    "SELECT a.id, ?, ?, ?, ? FROM accounts a "
                    "ON CONFLICT (account_id, key) DO UPDATE SET "
                    "name = EXCLUDED.name, category = EXCLUDED.category, description = EXCLUDED.description",
                    (skey, sname, scategory, sdescription),
                )
            # Self-healing bootstrap: whichever account owns the platform
            # operator's own login gets is_platform_owner — runs on every
            # boot (idempotent) so it applies whether that account was
            # created before or after this migration shipped, without a
            # one-off manual production update.
            conn.execute(
                "UPDATE accounts SET is_platform_owner = 1 WHERE id = ("
                "  SELECT account_id FROM users WHERE email = ?"
                ")",
                (_PLATFORM_OWNER_EMAIL,),
            )
            # If the first account already exists (a prior boot completed a
            # signup), any row still missing account_id predates that signup
            # — hand it over now rather than waiting for create_account_with_
            # owner to run again. No-op on a fresh install with zero accounts.
            first_account = conn.execute("SELECT MIN(id) AS id FROM accounts").fetchone()["id"]
            if first_account is not None:
                _backfill_legacy_account_id(conn, first_account)
    finally:
        conn.close()


