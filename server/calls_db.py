def check_call_allowed(account_id: int, phone: str) -> tuple[bool, str]:
    """Single gate every outbound dial passes through — DNC first (a hard,
    permanent block), then the calling window (a temporary time block).
    Returns (allowed, reason); reason is '' when allowed."""
    cfg = get_compliance(account_id)
    if cfg.get("honor_dnc", True) and is_dnc(account_id, phone):
        return False, "This number is on your Do-Not-Call list."
    return within_calling_window(account_id)


