"""Data layer for the dashboard API.

Shares one Postgres database with the voice agent (the agent writes call
rows; this module reads them and owns every other dashboard table) via
DATABASE_URL. Was SQLite through early development; moved to Postgres for
the durability/concurrency a sellable multi-tenant product needs (single-
writer lock, no replication, no failover — see
vistrow-voice-infrastructure-requirements.txt). dbconn.py is a thin shim
that keeps every call site below in its original sqlite3-flavored form
(`?` placeholders, `conn.execute(...)`, `with conn:` committing without
closing, `cursor.lastrowid`).

Lives in server/ rather than importing agent.db because agent's package
pulls in livekit-agents, which isn't installed in this venv; dbconn.py is
duplicated into agent/ instead so both stay independently importable.
"""

