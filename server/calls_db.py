# Every created_at/updated_at default below reproduces SQLite's
# datetime('now') output exactly ("YYYY-MM-DD HH:MM:SS", UTC, no fractional
# seconds) as a TEXT column — verified against a live Postgres instance —
# so every existing call site, the frontend's date parsing, and old SQLite-
# era row data all keep working unchanged; this is a storage-engine swap,
# not a schema redesign.
_NOW = "(to_char(now() AT TIME ZONE 'UTC', 'YYYY-MM-DD HH24:MI:SS'))"

