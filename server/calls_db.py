# ------------------------------------------------------------- compliance
#
# Everything an Indian SMB needs to run outbound legally: a Do-Not-Call
# registry every dial is scrubbed against, TRAI-style calling-window
# enforcement, and consent/recording/retention controls. The window +
# toggles live in the generic settings table under a "compliance." prefix
# (one JSON blob keeps it a single read); the DNC registry is its own table
# because it's list-shaped and queried on the dial hot path.

# TRAI telemarketing norms: unsolicited commercial calls only 09:00–21:00,
# Mon–Sat. These are the defaults; a tenant can tighten (never silently
# loosen past what they configure) via the Compliance settings page.
_COMPLIANCE_DEFAULTS = {
    "enforce_window": True,
    "window_start": "09:00",
    "window_end": "21:00",
    "active_days": ["Mon", "Tue", "Wed", "Thu", "Fri", "Sat"],
    "timezone": "Asia/Kolkata",
    "honor_dnc": True,
    "require_consent": False,
    "record_calls": False,
    # 0 = keep transcripts/recordings indefinitely; >0 = purge calls older
    # than N days (surfaced to the operator; enforced by purge_expired_calls).
    "retention_days": 0,
}
_COMPLIANCE_KEY = "compliance.config"
_DAY_ABBR = ["Mon", "Tue", "Wed", "Thu", "Fri", "Sat", "Sun"]


def _utc_now_str() -> str:
    """UTC 'now' in the exact text format the DB's own _NOW default emits
    ('YYYY-MM-DD HH:MM:SS'), so timestamps we generate in Python string-compare
    correctly against timestamps the DB wrote itself."""
    return datetime.datetime.now(datetime.timezone.utc).strftime("%Y-%m-%d %H:%M:%S")


def _normalize_phone(phone: str) -> str:
    """Digits-only form for DNC matching, tolerant of how the number was
    entered or how the telco presents it: strips spaces, punctuation, a
    leading + and country/trunk prefixes so "+91 98765 43210", "9876543210"
    and "098765 43210" all collapse to the same key. India-centric (10-digit
    subscriber numbers, 91 country code, 0 trunk) but degrades gracefully for
    other formats — it only ever strips, never rewrites."""
    digits = "".join(c for c in (phone or "") if c.isdigit())
    if len(digits) > 10:
        if digits.startswith("91") and len(digits) == 12:
            digits = digits[2:]
        elif digits.startswith("0") and len(digits) == 11:
            digits = digits[1:]
    return digits


def get_compliance(account_id: int) -> dict:
    """Merged over defaults so a partial/absent stored blob still returns a
    complete config the caller can rely on."""
    raw = get_setting(_COMPLIANCE_KEY, account_id)
    cfg = dict(_COMPLIANCE_DEFAULTS)
    if raw:
        try:
            stored = json.loads(raw)
            if isinstance(stored, dict):
                cfg.update({k: v for k, v in stored.items() if k in _COMPLIANCE_DEFAULTS})
        except (ValueError, TypeError):
            pass
    return cfg


def save_compliance(account_id: int, data: dict) -> dict:
    cfg = get_compliance(account_id)
    for k in _COMPLIANCE_DEFAULTS:
        if k in data:
            cfg[k] = data[k]
    set_setting(_COMPLIANCE_KEY, json.dumps(cfg), account_id)
    return cfg


def within_calling_window(account_id: int, at: datetime.datetime | None = None) -> tuple[bool, str]:
    """(allowed, human_reason). Uses the tenant's configured timezone so a
    9-to-9 window means 9-to-9 where the *callee* is, not on the server."""
    cfg = get_compliance(account_id)
    if not cfg.get("enforce_window", True):
        return True, ""
    try:
        tz = ZoneInfo(cfg.get("timezone") or "Asia/Kolkata")
    except Exception:
        tz = ZoneInfo("Asia/Kolkata")
    now = (at or datetime.datetime.now(datetime.timezone.utc)).astimezone(tz)
    day = _DAY_ABBR[now.weekday()]
    active = cfg.get("active_days") or _COMPLIANCE_DEFAULTS["active_days"]
    if day not in active:
        return False, f"Outside allowed calling days ({', '.join(active)}) — today is {day}."
    try:
        sh, sm = (int(x) for x in str(cfg.get("window_start", "09:00")).split(":"))
        eh, em = (int(x) for x in str(cfg.get("window_end", "21:00")).split(":"))
    except (ValueError, TypeError):
        return True, ""
    start = now.replace(hour=sh, minute=sm, second=0, microsecond=0)
    end = now.replace(hour=eh, minute=em, second=0, microsecond=0)
    if start <= now <= end:
        return True, ""
    win = f"{cfg.get('window_start')}–{cfg.get('window_end')} {cfg.get('timezone')}"
    return False, f"Outside the allowed calling window ({win})."


def is_dnc(account_id: int, phone: str) -> bool:
    norm = _normalize_phone(phone)
    if not norm:
        return False
    conn = _connect()
