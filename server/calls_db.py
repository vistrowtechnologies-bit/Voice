def update_agent(agent_id: int, data: dict, account_id: int) -> dict | None:
    conn = _connect()
    try:
        sets, params = [], []
        for key, value in data.items():
            column = _AGENT_CAMEL_TO_SNAKE.get(key, key)
            if column not in _AGENT_FIELDS:
                continue
            if column == "kb_id" and value is not None:
                # Refuse to point this agent at a knowledge base the caller's
                # account doesn't own — otherwise a crafted kbId would let one
                # tenant's agent read another tenant's KB content at call time.
                owned = conn.execute(
                    "SELECT 1 FROM knowledge_bases WHERE id = ? AND account_id = ?", (value, account_id)
                ).fetchone()
                if not owned:
                    continue
            if column in _AGENT_BOOL_FIELDS:
                # Postgres has no boolean->integer cast, and these columns are
                # INTEGER — a raw Python bool from the JSON body fails the
                # UPDATE outright rather than coercing. Same pattern as
                # set_kb_strict.
                value = 1 if value else 0
            elif column in _AGENT_JSON_FIELDS:
                # Frontend sends a real array/object; store it as a JSON string
                # (already-a-string values pass through untouched).
                if not isinstance(value, str):
                    value = json.dumps(value or [])
            sets.append(f"{column} = ?")
            params.append(value)
        if not sets:
            return None
        with conn:
            if data.get("is_platform_demo") or data.get("isPlatformDemo"):
                # At most one agent platform-wide answers the public marketing
                # demo — clear any previous holder before this one claims it.
                conn.execute("UPDATE agents SET is_platform_demo = 0 WHERE id != ?", (agent_id,))
            conn.execute(
                f"UPDATE agents SET {', '.join(sets)}, updated_at = {_NOW} WHERE id = ? AND account_id = ?",
                (*params, agent_id, account_id),
            )
        row = conn.execute("SELECT * FROM agents WHERE id = ? AND account_id = ?", (agent_id, account_id)).fetchone()
        return _agent_dict(row) if row else None
    finally:
        conn.close()


