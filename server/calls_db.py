_RESET_TTL_SECONDS = 60 * 60  # 1 hour


def create_password_reset(user_id: int) -> str:
    """Mint a single-use reset token for a user. Returns the raw token (goes in
    the emailed link); only its hash is stored."""
    token = secrets.token_urlsafe(32)
    conn = _connect()
    try:
        with conn:
            conn.execute(
                "INSERT INTO password_resets (user_id, token_hash, expires_at) VALUES (?, ?, ?)",
                (user_id, _hash_api_key(token), int(time.time()) + _RESET_TTL_SECONDS),
            )
        return token
    finally:
        conn.close()


def consume_password_reset(token: str) -> int | None:
    """Validate + burn a reset token, returning the user_id if it's valid,
    unexpired, and unused; otherwise None."""
    if not token:
        return None
    conn = _connect()
    try:
        row = conn.execute(
            "SELECT id, user_id, expires_at, used FROM password_resets WHERE token_hash = ?",
            (_hash_api_key(token),),
        ).fetchone()
        if row is None or row["used"] or row["expires_at"] < int(time.time()):
            return None
        with conn:
            conn.execute("UPDATE password_resets SET used = 1 WHERE id = ?", (row["id"],))
        return row["user_id"]
    finally:
        conn.close()


