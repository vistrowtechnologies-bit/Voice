def _visitor_numbers_by_id(conn, account_id: int) -> dict[int, int]:
    """Stable per-account ordinal (creation order) for calls with no captured
    lead name — lets the caller column read "Visitor #N" instead of the
    client-generated random room identity ("visitor-a1b2c3d4"), which told a
    tenant nothing about how many distinct anonymous visitors they'd had."""
    rows = conn.execute(
        "SELECT id FROM calls WHERE account_id = ? AND (lead_name IS NULL OR lead_name = '') "
        # %% escapes the literal % for psycopg's own %s-style placeholder
        # parsing — a bare % here collides with it (see dbconn.py's Conn.execute).
        "AND visitor_identity LIKE 'visitor-%%' ORDER BY id",
        (account_id,),
    ).fetchall()
    return {r["id"]: i + 1 for i, r in enumerate(rows)}


def _call_dict(
    row: dict,
    include_transcript: bool = True,
    sites_by_id: dict[int, dict] | None = None,
    agent_names: dict[int, str] | None = None,
    visitor_numbers: dict[int, int] | None = None,
) -> dict:
    transcript = json.loads(row["transcript_json"]) if row["transcript_json"] else []
    if row["lead_name"]:
        name = row["lead_name"]
    elif visitor_numbers and row["id"] in visitor_numbers:
        name = f"Visitor #{visitor_numbers[row['id']]}"
    else:
        name = row["visitor_identity"] or "Unknown caller"
    call_type = row["call_type"] or "browser"
    site_id = row["site_id"]
    site = sites_by_id.get(site_id) if sites_by_id and site_id else None
    website = (site.get("allowedDomain") or site.get("name")) if site else ""
    # Rows from before the agent_id column (or with a since-deleted agent)
    # fall back to the first agent's name — historically the only one.
    agent_names = agent_names or {}
    agent_name = agent_names.get(row["agent_id"]) or next(iter(agent_names.values()), "Agent")
    out = {
        "id": str(row["id"]),
        "name": name,
        "initials": _initials(name),
        "phone": row["lead_phone"] or "",
        "email": row["lead_email"] or "",
        "budget": row["lead_budget"] or "",
        "location": row["lead_location"] or "",
        "timeline": row["lead_timeline"] or "",
        "company": row["lead_company"] or "",
        "useCase": row["lead_use_case"] or "",
        "teamSize": row["lead_team_size"] or "",
        "extractedData": _load_json_field(row["extracted_data"], {}),
        "intelligence": _load_json_field(_row_get(row, "intelligence_json"), None),
        "status": _lead_status(row),
        "callStatus": _status(row, transcript),
        "sentiment": _sentiment(transcript),
        "channel": _CHANNEL_LABELS.get(call_type, "Web"),
        "callType": call_type,
        "siteId": site_id,
        "website": website,
        "agent": agent_name,
        "agentId": row["agent_id"],
        "callDate": row["started_at"],
        "durationSeconds": row["duration_seconds"],
        "replyLanguage": row["reply_language"],
        "siteVisit": json.loads(row["site_visit_json"]) if row["site_visit_json"] else None,
        "arthaleadsStatus": _row_get(row, "arthaleads_status"),
        "arthaleadsSyncedAt": _row_get(row, "arthaleads_synced_at"),
        "arthaleadsError": _row_get(row, "arthaleads_error"),
        # Never the raw R2 key — just whether one exists, so the frontend
        # knows to show a player and fetch a presigned URL on demand.
        "hasRecording": bool(_row_get(row, "recording_key")),
    }
    if include_transcript:
        out["transcript"] = [
            {
                "speaker": "agent" if t.get("role") == "assistant" else "visitor",
                "text": t.get("text", ""),
            }
            for t in transcript
        ]
    return out


def list_calls(account_id: int, limit: int = 200, search: str = "", status: str = "", days: int = 0) -> list[dict]:
    sites_by_id = {s["id"]: s for s in list_sites(account_id)}
    agent_names = _agent_names_by_id(account_id)
    conn = _connect()
    try:
        visitor_numbers = _visitor_numbers_by_id(conn, account_id)
        query = "SELECT * FROM calls WHERE account_id = ?"
        params: list = [account_id]
        if days:
            query += " AND started_at::date >= (CURRENT_DATE - (? || ' days')::interval)::date"
            params.append(str(days - 1))
        query += " ORDER BY started_at DESC LIMIT ?"
        params.append(limit)
        rows = conn.execute(query, params).fetchall()
        calls = [
            _call_dict(
                r,
                include_transcript=False,
                sites_by_id=sites_by_id,
                agent_names=agent_names,
                visitor_numbers=visitor_numbers,
            )
            for r in rows
        ]
        if search:
            s = search.lower()
            calls = [c for c in calls if s in c["name"].lower() or s in c["phone"]]
        if status:
            calls = [c for c in calls if c["callStatus"] == status or c["status"] == status]
        return calls
    finally:
        conn.close()


