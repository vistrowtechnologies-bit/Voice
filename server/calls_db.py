def update_member_role(account_id: int, user_id: int, role: str) -> None:
    """Only admin/member/viewer are settable here — ownership never transfers
    through this path. Raises ValueError if the target is the owner or the
    role is invalid, which the route surfaces as a 400."""
    if role not in ("admin", "member", "viewer"):
        raise ValueError("invalid role")
    conn = _connect()
