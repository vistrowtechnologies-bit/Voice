def create_campaign(data: dict, account_id: int) -> int:
    """Create a campaign and load its call queue. The queue comes from either
    an explicit `contacts` list ([{name, phone}], e.g. a CSV upload) or a
    `contactTag` that pulls matching rows from the contacts table. Phones are
    deduped by normalized form so the same person isn't dialed twice. Returns
    the new campaign id. Starts in 'draft' — the dialer ignores it until the
    operator explicitly launches it (set_campaign_status running)."""
    conn = _connect()
    try:
        with conn:
            cur = conn.execute(
                """
                INSERT INTO campaigns
                    (account_id, name, agent_id, from_number, contact_tag, scheduled_date,
                     max_attempts, retry_minutes, concurrency, status)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, 'draft')
                RETURNING id
                """,
                (
                    account_id,
                    data.get("name", "Untitled campaign"),
                    data.get("agentId"),
                    data.get("fromNumber", ""),
                    data.get("contactTag", ""),
                    data.get("scheduledDate"),
                    max(1, int(data.get("maxAttempts", 1) or 1)),
                    max(1, int(data.get("retryMinutes", 60) or 60)),
                    max(1, int(data.get("concurrency", 1) or 1)),
                ),
            )
            campaign_id = cur.fetchone()["id"]

            rows: list[tuple[str, str]] = []  # (name, phone)
            explicit = data.get("contacts")
            if isinstance(explicit, list) and explicit:
                for c in explicit:
                    phone = str((c or {}).get("phone") or "").strip()
                    if phone:
                        rows.append((str((c or {}).get("name") or "").strip(), phone))
            else:
                tag = data.get("contactTag", "")
                q = "SELECT name, phone FROM contacts WHERE account_id = ? AND phone IS NOT NULL AND phone <> ''"
                params: list = [account_id]
                if tag:
                    q += " AND tags LIKE ?"
                    params.append(f"%{tag}%")
                for r in conn.execute(q, tuple(params)).fetchall():
                    rows.append((r["name"], r["phone"]))

            seen: set[str] = set()
            for name, phone in rows:
                norm = _normalize_phone(phone)
                if not norm or norm in seen:
                    continue
                seen.add(norm)
                conn.execute(
                    "INSERT INTO campaign_contacts (campaign_id, account_id, name, phone, phone_norm) "
                    "VALUES (?, ?, ?, ?, ?)",
                    (campaign_id, account_id, name, phone.strip(), norm),
                )
            return campaign_id
    finally:
        conn.close()


